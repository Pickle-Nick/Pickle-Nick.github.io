// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//function that returns a Boolean if number is even or odd
function isEven(num) {
  //conditional statements telling whether the number is is even
if ( num % 2 === 0) {
  return true;
} else if (num % 2 == 1) {
  return false;
} else {
  return false;
}
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  //create a count variable to incriment
  var count = 0;
  //loop through input string
  for (var i = 0; i < string.length; i++) {
    if (string[i] === char) {
      count++;
    }
  } return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  //create a count variable to incriment everytime the loop hits the desired character
  var count = 0;
  //loop through the charcters of the string
  for (var i = 0; i < string.length; i++) {
    if (string[i] === 'B') {
      count++;
    }
  }
  return count;
}

